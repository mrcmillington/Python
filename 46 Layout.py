from tkinter import *
root = Tk()
topFrame = Frame(root)
topFrame.pack()
bottomFrame = Frame(root)
bottomFrame.pack(side=BOTTOM)
button1 = Button(topFrame,text="Click me",fg="blue")
button2 = Button(topFrame,text="Hit me",fg="red")
button1.pack(side=LEFT)
button2.pack()
root.mainloop()

